int	ft_str_is_alpha(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || ('Z' < str[i] && str[i] < 'a') || str[i] > 'z')
			j++;
		i++;
	}
	if (j == 0)
		return (1);
	else
		return (0);
}
